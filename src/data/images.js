const optimizeCloudinary = (url, transform) => {
  if (!url || !url.includes('res.cloudinary.com')) {
    return url
  }

  if (url.includes('/upload/f_auto')) {
    return url
  }

  return url.replace('/upload/', `/upload/${transform}/`)
}

